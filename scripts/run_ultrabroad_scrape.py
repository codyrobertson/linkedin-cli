QUERY_PROGRESS_INTERVAL = 10
CAMPAIGN_EXPANSION = "recursive"
QUERY_WAVE_SIZE = 100_000
WAVE_INDUSTRY_COUNT = 8
WAVE_INDEX = 0
JOB_PREFIX = f"million-corpus-searxng-ultrabroad-recursive-v4-wave{WAVE_INDEX + 1}"
SEARXNG_URL = os.environ.get("ULTRABROAD_SEARXNG_URL") or os.environ.get("SEARXNG_URL") or "http://127.0.0.1:8080"


def build_queries() -> list[str]:
    start_industry = max(0, int(WAVE_INDEX)) * max(1, int(WAVE_INDUSTRY_COUNT))
    end_industry = start_industry + max(1, int(WAVE_INDUSTRY_COUNT))
    wave_industries = INDUSTRIES[start_industry:end_industry] or INDUSTRIES[: max(1, int(WAVE_INDUSTRY_COUNT))]
    queries = content.build_harvest_queries(
        industries=wave_industries,
        topics=TOPICS,
        expansion=CAMPAIGN_EXPANSION,
    )
    queries = _filter_queries(queries)
    return queries[: max(1, int(QUERY_WAVE_SIZE))]


def emit_progress(event: dict[str, object]) -> None:
    event_type = str(event.get("event") or "")
    if event_type == "campaign_started":
        print(
            f"[content] campaign started: {event['job_count']} jobs across {event['query_count']} queries "
            f"limit={event['limit']}",
            flush=True,
        )
    elif event_type == "campaign_job_started":
        print(
            f"[content] campaign job {event['job_index']}/{event['job_count']}: "
            f"{event['job_id']} ({event['query_count']} queries)",
            flush=True,
        )
    elif event_type == "query_results" and int(event.get("result_count") or 0) > 0:
        print(f"[content] results: {event['result_count']} for {event['query']}", flush=True)
    elif event_type == "query_started":
        query_index = int(event.get("query_index") or 0)
        query_count = int(event.get("query_count") or 0)
        if query_index <= 3 or (QUERY_PROGRESS_INTERVAL > 0 and query_index % QUERY_PROGRESS_INTERVAL == 0):
            print(
                f"[content] query {query_index}/{query_count}: {event['query']}",
                flush=True,
            )
    elif event_type in {"query_failed", "query_backend_failed"} and event.get("query"):
        print(
            f"[content] {event_type}: {event['query']} :: {event.get('error')}",
            flush=True,
        )
    elif event_type == "campaign_job_completed":
        print(
            f"[content] campaign job complete: {event['job_id']} stored {event['stored_count']} "
            f"total {event['stored_total']}/{event['limit']}",
            flush=True,
        )
    elif event_type == "campaign_complete":
        print(f"[content] campaign complete: {event['stored_count']} stored across {event['job_count']} jobs", flush=True)


def main() -> None:
    queries = build_queries()
    print(
        f"[content] launching broad taxonomic campaign with {len(INDUSTRIES)} industries, "
        f"{len(TOPICS)} topics, {len(queries)} queries",
        flush=True,
    )
    summary = content.harvest_campaign(
        industry=INDUSTRIES[0],
        industries=INDUSTRIES,
        topics=TOPICS,
        query_terms=queries,
        limit=1_000_000,
        per_query=100,
        per_job_limit=2_000,
        queries_per_job=100,
        query_workers=2,
        job_prefix=JOB_PREFIX,
        progress=emit_progress,
        backend="public-only",
        public_search="searxng",
        searxng_url=SEARXNG_URL,
        resume=True,
    )
    print(summary, flush=True)


if __name__ == "__main__":
    main()
