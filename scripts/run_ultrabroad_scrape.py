def _query_domain(term: str, industry_terms: set[str], topic_terms: set[str]) -> str:
    if term in industry_terms:
        if term in GTM_INDUSTRY_HINTS:
            return "gtm"
        if term in TECH_INDUSTRY_HINTS:
            return "tech"
        return "general"
    if term in topic_terms:
        if term in GTM_TOPIC_HINTS:
            return "gtm"
        if term in TECH_TOPIC_HINTS:
            return "tech"
        return "general"
    return "general"


def _filter_queries(queries: list[str]) -> list[str]:
    industry_terms = set(INDUSTRIES)
    topic_terms = set(TOPICS)
    filtered: list[str] = []
    for query in queries:
        text = query.replace("site:linkedin.com/posts ", "").replace("site:linkedin.com/feed/update ", "").strip()
        lowered = text.lower()
        matched_industry = next((term for term in sorted(industry_terms, key=len, reverse=True) if lowered.startswith(term)), None)
        if not matched_industry:
            filtered.append(query)
            continue
        remainder = lowered[len(matched_industry) :].strip().strip('"')
        matched_topic = next((term for term in sorted(topic_terms, key=len, reverse=True) if remainder.startswith(term)), None)
        if not matched_topic:
            filtered.append(query)
            continue
        industry_domain = _query_domain(matched_industry, industry_terms, topic_terms)
        topic_domain = _query_domain(matched_topic, industry_terms, topic_terms)
        if "site:linkedin.com/posts" in query and industry_domain == "tech" and topic_domain == "gtm":
            continue
        filtered.append(query)
    return filtered


